#!/usr/bin/perl

use 5.010;
use strict;
use warnings;

#Integers
my $int = 10;
print $int,"\n";

#Negative numbers
my $neg = -10;
print $neg,"\n";

#Floating point
my $fp = 100.433;
print $fp,"\n";

#Scientific notation
my $sn = 10E22;
print $sn,"\n";

#Hexadecimal
my $hex = 0x000F;
print $hex,"\n";

#Octal
my $oct = 0310;
print $oct,"\n";
